function getCamposFormulario(tipoGestion, etapa, area) {
  const form = FORMULARIOS[tipoGestion];
  if (!form) return [];
  if (tipoGestion === 'OFICIO' && area === 'PENAL') {
    return (form.variante_penal && form.variante_penal[etapa]) ? form.variante_penal[etapa] : [];
  }
  return form[etapa] || [];
}


